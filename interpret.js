function counts(positions){
  const e=[0,0,0,0], m=[0,0,0,0], mo=[0,0,0]; // elem(火土風水), mode(活動固定柔軟)
  for(const b of ASTRO_BODIES){ const s=SIGNS[positions[b].sign]; e[s.elem]++; mo[s.mode]++; }
  return {elem:e, mode:mo};
}

/** 鑑定文（HTML文字列の配列の章立て）を生成 */
function buildReading(positions, timeKnown){
  const sun = SIGNS[positions.Sun.sign].jp;
  const moon = SIGNS[positions.Moon.sign].jp;
  const merc = SIGNS[positions.Mercury.sign].jp;
  const ven = SIGNS[positions.Venus.sign].jp;
  const mars = SIGNS[positions.Mars.sign].jp;
  const c = counts(positions);
  const elemNames=['火','土','風','水'], modeNames=['活動宮','固定宮','柔軟宮'];

  // 元素の最多・欠落
  const maxE = Math.max(...c.elem), minE = Math.min(...c.elem);
  const domElem = elemNames[c.elem.indexOf(maxE)];
  const lackElems = elemNames.filter((_,i)=>c.elem[i]===0);
  // 区分の最多
  const domMode = modeNames[c.mode.indexOf(Math.max(...c.mode))];
  // 陰陽
  const yang = c.elem[0]+c.elem[2], yin = c.elem[1]+c.elem[3];
  const polar = yang-yin>=3 ? POLAR.yang : yin-yang>=3 ? POLAR.yin : POLAR.bal;

  const sections = [];

  sections.push({ h:'１．あなたという人の核（太陽 in '+sun+'）', p:[
    SUN_TEXT[sun],
    '太陽はホロスコープのエンジンであり、人生をかけて磨いていく「主役の役柄」です。歳を重ねるほど、この'+sun+'らしさが板についていきます。'
  ]});

  sections.push({ h:'２．素のあなた・心のクセ（月 in '+moon+'）', p:[
    MOON_TEXT[moon],
    '月は無自覚な「素顔」。自分では気づきにくいのに、身近な人にはよく見えている部分です。'+(timeKnown?'':'※出生時刻が不明のため、月の度数はおおよそです（正午で計算）。日付によっては前後のサインの可能性もあります。')
  ]});

  sections.push({ h:'３．知性・愛・行動の三点セット', p:[
    '<b>水星（'+merc+'）</b>＝知性と話し方：'+MERCURY_TEXT[merc]+'。',
    '<b>金星（'+ven+'）</b>＝愛と美意識：'+VENUS_TEXT[ven]+'タイプ。',
    '<b>火星（'+mars+'）</b>＝情熱と戦い方：'+MARS_TEXT[mars]+'スタイル。',
    'この三つは日常のあなたを形づくる実働部隊。太陽（核）と月（素顔）の間で、考え・愛し・行動する具体的なクセを示します。'
  ]});

  const elemP = [];
  if (maxE - minE <= 1 && !lackElems.length) {
    elemP.push('四元素すべてに天体がバランスよく散らばった調和型。突出も欠けもなく、状況に応じて火・土・風・水の面を自在に使い分けられる懐の深さがあります。');
  } else {
    elemP.push(ELEM_DOMINANT[domElem]);
    if (lackElems.length) elemP.push('一方で、'+lackElems.map(e=>ELEM_LACK[e]).join(' '));
  }
  sections.push({ h:'４．エネルギーの質（四元素のバランス）', p:[
    '火'+c.elem[0]+'・土'+c.elem[1]+'・風'+c.elem[2]+'・水'+c.elem[3]+'（10天体の配分）。', ...elemP
  ]});

  sections.push({ h:'５．動き方のスタイル（3区分と陰陽）', p:[
    '活動宮'+c.mode[0]+'・固定宮'+c.mode[1]+'・柔軟宮'+c.mode[2]+'。', MODE_DOMINANT[domMode],
    '陽'+yang+'・陰'+yin+'。'+polar
  ]});

  // 総括
  sections.push({ h:'６．統合鑑定 — あなたへ', p:[
    'まとめましょう。あなたは<b>'+sun+'</b>の核を持ち、<b>'+moon+'</b>の感性で世界を感じる人。'+
    domElem+'の要素と'+domMode+'の性質が前面に出て、'+(yang>yin?'外へ向かう推進力':yin>yang?'内に蓄える調整力':'攻守のバランス')+'があなたの持ち味です。',
    '星はあくまで「設計図」であり、宿命ではありません。強みを伸ばし、薄い部分を意識的に補えば、ホロスコープはあなたの人生最強の地図になります。あなたが本来の輝きを存分に発揮されることを、星々とともに願っています。'
  ]});

  return sections;
}
